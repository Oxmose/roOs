int snprintf(char* pBuffer, size_t size, const char * kpFmt, ...)
{
    int retVal;
    __builtin_va_list args;

    __builtin_va_start(args, kpFmt);
    retVal = vsnprintf(pBuffer, size, kpFmt, args);
    __builtin_va_end(args);

    return retVal;
}

